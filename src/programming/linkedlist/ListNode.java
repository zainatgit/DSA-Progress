package programming.linkedlist;

//Learn the recursive way also from leetcode solutions
public class ListNode {
    public int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
